# 3. 문자열 타입을 (정수,실수,불리언) 으로 변경 하는 방법
    # "123"(str문자열) --> 123(int정수)
    # int() : 문자열 타입을 정수 타입으로 변경 해서 반환/돌려주는 함수
    # float() : 문자열 타입을 실수 타입으로 변경 해서 반환/돌려주는 함수
    # bool() : 문자열 타입을 불리언 타입으로 변경 해서 반환/돌려주는 함수
print (type(int( "123" ))) #<class 'int'> #주의할점 '('열고 ')' 닫기를 개수를 맞추어 작성하기
print(type(float("3.14"))) #<class 'float'> #문자열 타입을 실수타입으로 변경
print(type(bool("True"))) #<class 'bool'> #문자열 타입을 불리언타입으로 변경