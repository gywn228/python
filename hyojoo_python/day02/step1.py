# 1. 데이터들의 타입 확인, type (리터럴값)
print(type(123)) #<class 'int'> 는 정수타입을 의미한다
print(type("123")) #<class 'str'> 는 문자열타입을 의미한다
print(type(3.14)) #<class 'float'> 는 실수타입을 의미한다
print(type(True)) #<class 'bool'> 는 불리언타입을 의미한다



