# 샘플 : 아메리카노(5.8), 카페라떼(6.3), 자몽차(5.5)

# 리스트 = [ ]
coffee = ['아메리카노' , '카페라떼' , '자몽차']
price = [5800,6300,5500]
print(f'{coffee[0]}은 {price[0]}원 이고, {coffee[1]}은 {price[1]}원 이고, {coffee[2]}는 {price[2]}원 입니다.')

