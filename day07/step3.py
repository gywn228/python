# 가게정보 = [ {'아메리카노' : 5800 , '카페라떼' : 6300 , '자몽차' : 5500} ]

# 튜플 = () 리스트와 동일 해서 생략 , 차이점 : 요소의 수정/삭제 가 안된다

# 딕셔너리 = { } # 자료들 중에 누가 key고 누가 value 인지 선정 # key는 중복 값이 없어야 한다 (가격은 중복이 있으므로 제품명을 key로 선정)
제품목록 = {'아메리카노' : 5800 , '카페라떼' : 6300 , '자몽차' : 5500} # key = 제품명 , value = 가격
print(제품목록)

# 변수 = "문자열" :  하나의 문자열 에 여러개의 자료들을 * 구분(문)자를 넣어서 여러개의 자료들을 넣는다
변수 = "아메리카노,5800원\n카페라떼,6300원\n자몽차,5500원" # 문자열 1개 자료
    # 제품명과 가격 구분은 ,(쉼표)
    # 제품 간의 구분은 \n(줄바꿈)
    # 내 마음 대로 구분자를 정해서 사용이 가능 하지만 , 일반적으로 주로 사용되는 형식 CSV 형식
print(변수)

